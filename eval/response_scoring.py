import re

def step_count(text):
    """
    Count the number of 'Step' statements in a response, useful for CoT prompts.
    Returns 0 if none are found.
    """
    return len(re.findall(r"Step\s*\d*[:\-]", text, flags=re.IGNORECASE))

def keyword_coverage_score(response, keywords):
    """
    Check how many of the expected keywords are present in the LLM's response.
    Returns a float between 0 and 1.
    """
    if not keywords:
        return 0.0
    found = sum(1 for kw in keywords if kw.lower() in response.lower())
    return found / len(keywords)